public class Main {
    public static void main(String[] args) {
        //Math.PI
        System.out.println("Số pi: " + Math.PI);

        //Math.abs()
        System.out.println("|10|: " + Math.abs(10));

        //Math.ceil()
        System.out.println("4.8 sau khi làm tròn là : "+Math.ceil(4.8));

        //Math.floor()
        System.out.println("4.3 sau khi làm tròn là: "+Math.floor(4.3));

        //Math.max()
        System.out.println("Số lớn nhất trong hai số 4 và 6 là: "+Math.max(4,6));

        //Math.min()
        System.out.println("Số nhỏ nhất trong hai sô 4 và 6 là: "+Math.min(4,6));

        //Math.pow()
        System.out.println("5^3 = " + Math.pow(5,3)); //5 là cơ số, 3 là số mũ

        //Math.sqrt()
        System.out.println("Căn bậc 2 của 16 là: " + Math.sqrt(16));

        //Math.sin()
        System.out.println("Đơn vị góc: " + Math.sin(Math.PI/2));

        //Math.cos()
        System.out.println("Đơn vị góc: " + Math.cos(Math.PI));

        //Math.random()
        System.out.println("Sinh sô ngẫu nhiên: " + Math.random());

        //Math.toDegrees()
        System.out.println("Đổi radian thành độ: " + Math.toDegrees(Math.PI));

        //Math.toRadian()
        System.out.println("Đổi độ sang radian: " + Math.toRadians(180));
    }
}
